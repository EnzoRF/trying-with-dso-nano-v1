//*****************************************************************************
static int sin1000 (unsigned int ang)
{
	if (ang > 360)
		ang %= 360;
	if ((ang >= 0) && (ang <= 90))
		return tg[ang].sin;
	if ((ang > 90) && (ang <= 180))
		return tg[180-ang].sin;
	if ((ang > 180) && (ang <= 270))
		return -tg[ang-180].sin;
	if ((ang > 270) && (ang <= 360))
		return -tg[360-ang].sin;
}

