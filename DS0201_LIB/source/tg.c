static int sin1000 (int ang)
{
	while (ang < 0) {
		ang += 360;
	}
	if (ang > 360)
		ang %= 360;
	if ((ang >= 0) && (ang <= 90))
		return sint[ang];
	if ((ang > 90) && (ang <= 180))
		return sint[180-ang];
	if ((ang > 180) && (ang <= 270))
		return -sint[ang-180];
	if ((ang > 270) && (ang <= 360))
		return -sint[360-ang];
}
